#!/usr/bin/env python

def square_area(side):
    area = side * side
    return area

def rectangular_perimeter(length, height):
    perm = (length * 2) + (height * 2)
    return perm

def square_perimeter(length):
    return length * 4

def circle_area(radius):
    return radius * radius * 3.141

def circle_circumference(radius):
    diam = radius * 2
    return 3.141 * diam

